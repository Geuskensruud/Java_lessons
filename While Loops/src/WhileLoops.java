
public class WhileLoops {

	public static void main(String[] args) {
		
		int value = 0;
		
		while(value <10) {
			System.out.println("the int value of : " + value + " is still smaller as 10");
			
			value = value +1;
		}
		System.out.println("the int value of : " + value + " is equal or larger 10");

	}

}
